export function getPersonalityText(pillars: SaJuPillar, yongShin: YongShin): string {
  const dayGan = pillars.day.gan;
  const base = DAYGAN_PERSONA[dayGan] || '';
  const ohaeng = CHEONGAN_ATTR[dayGan].ohaeng;
  const strengthKey = yongShin.isGangShin ? 'strong' : 'weak';
  const ohaengText = OHAENG_STRENGTH[ohaeng][strengthKey];
  const gangWeak = yongShin.isGangShin
    ? '전반적으로 자기 주도적이고 의지가 강한 편입니다.'
    : '환경과 주변의 도움을 잘 활용하면 더 큰 성과를 이룰 수 있습니다.';

  return `${base}\n\n${ohaengText} ${gangWeak}`;
}
